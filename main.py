# Завдання 1
# Введення чотирьох чисел (цілих і дробових)
a = float(input("Введіть перше число (a): "))
b = float(input("Введіть друге число (b): "))
c = float(input("Введіть третє число (c): "))
d = float(input("Введіть четверте число (d): "))

# Завдання 2
# Обчислення
results = [
    a + b,  # додавання
    b - c,  # віднімання
    c * d,  # множення
    a / d if d != 0 else "ділення на нуль",  # ділення
    b**2,  # піднесення до степеня
    int(c) // int(a) if a != 0 else "ділення на нуль",  # цілочисленне ділення
    int(d) % int(b) if b != 0 else "ділення на нуль"  # остача від ділення
]

print("Результати обчислень:", results)

# Завдання 3
print("\nКількість елементів у списку:", len(results))

# Вивід парних елементів (тільки числові, не повідомлення про помилку)
even_elements = [
    x for x in results if isinstance(x, (int, float)) and x % 2 == 0
]
print("Парні елементи списку:", even_elements)

# Завдання 4
# Поміняємо місцями другий і п’ятий елементи
if len(results) >= 5:
    results[1], results[4] = results[4], results[1]
print("Список після обміну елементів:", results)

